<?php

namespace Tests\Feature\Http\Requests\Genre\v1;

use App\Http\Requests\Genre\v1\GenreBaseRequest;
use App\Http\Requests\Genre\v1\GenreStoreRequest;
use Faker\Factory;
use Illuminate\Foundation\Testing\DatabaseMigrations;
use Illuminate\Support\Facades\Validator;
use Tests\TestCase;
use Faker\Factory as FakerFactory;
use Illuminate\Routing\Redirector;
use Illuminate\Validation\ValidationException;
use function app;

/**
 * @group requests
 * @group genre
 */
class GenreStoreRequestTest extends TestCase
{

    use DatabaseMigrations;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
    }

    public function test_GenreStoreRequest_empty()
    {
        try {
            $request = new GenreStoreRequest([]);
            $request
                ->setContainer(app())
                ->setRedirector(app(Redirector::class))
                ->validateResolved();
        } catch (ValidationException $ex) {
            print_r($ex->errors(), true);
        }
        //\Log::debug(print_r($ex->errors(), true));
        self::assertTrue(isset($ex));
        self::assertArrayHasKey('name', $ex->errors());
    }


    public function test_GenreStoreRequest_success()
    {
        $faker = FakerFactory::create();
        $param = [
            'name' => $faker->colorName,
            'is_active' => $faker->boolean,
        ];
        try {
            $request = new GenreStoreRequest($param);
            $request
                ->setContainer(app())
                ->setRedirector(app(Redirector::class))
                ->validateResolved();
        } catch (ValidationException $ex) {
        }

        $this->assertFalse(isset($ex));
    }

    /**
     * @dataProvider provideValidData
     */
    public function testValidData(array $data)
    {
        $request = new GenreStoreRequest();

        $validator = Validator::make($data, $request->rules());

        $this->assertTrue($validator->passes());
    }

    public function provideValidData()
    {
        $faker = \Faker\Factory::create(Factory::DEFAULT_LOCALE);

        return [
            [[
                'name' => 'test',
            ]],
            [[
                'name' => $faker->colorName,
                'is_active' => $faker->boolean,
            ]],
            [[
                'name' => $faker->colorName,
                'is_active' => $faker->boolean,
            ]]
            // ...
        ];
    }
    /**
     * @dataProvider provideInvalidData
     */
    public function testInvalidData(array $data)
    {
        $request = new GenreStoreRequest();

        $validator = Validator::make($data, $request->rules());


        $this->assertFalse($validator->passes());
    }

    public function provideInvalidData()
    {
        $faker = \Faker\Factory::create(Factory::DEFAULT_LOCALE);
        return [
            [[]], // missing fields
            [[
                'title' => 'test' // missing body
            ]],
            [[
                'is_active' => $faker->boolean, // missing name
            ]],
            [[
                'name' => str_repeat('a', GenreBaseRequest::NAME_MAX_LENGTH + 1), // title too long
            ]],
            [[
                'name' => $faker->colorName,
                'is_active' => 'true',
            ]],
            [[
                'name' => $faker->colorName,
                'is_active' => 'false',
            ]]
            // ...
        ];
    }
}
